# ---------------------------------------------------------------------------

def zlib_diff_config(
    c_source_dir: Path,
    *,
    include_dirs: list[Path] | None = None,
) -> DifferentialConfig:
    """Build a DifferentialConfig tailored to zlib.

    Args:
        c_source_dir: directory containing zlib's C source (*.c + *.h).
        include_dirs: additional include paths for the C DLL build.
    """
    c_sources = sorted(
        f for f in Path(c_source_dir).glob("*.c")
        if "test" not in f.name.lower()
        and "example" not in f.name.lower()
    )
    return DifferentialConfig(
        c_sources=c_sources,
        c_include_dirs=list(include_dirs or [Path(c_source_dir)]),
        c_public_signatures=zlib_public_signatures(),
        c_typedefs=zlib_typedefs(),
        c_opaque_types=set(),
        harnesses=zlib_harnesses(),
        ffi_crate_name="c_zlib_ref",
    )
