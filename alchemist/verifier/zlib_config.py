"""Zlib-specific DifferentialConfig — ready-to-plug into Stage 5.

Wires up the C FFI signatures and AlgorithmHarness configs that differentially
test generated zlib Rust against C zlib (via zlib1.dll / libz).

Phase D usage:

    from alchemist.verifier.differential_tester import verify_workspace
    from alchemist.verifier.zlib_config import zlib_diff_config

    report = verify_workspace(output_dir, diff_config=zlib_diff_config())
    assert report.passed  # only true if ALL 4 gates pass
"""

from __future__ import annotations

from pathlib import Path

from alchemist.verifier.auto_ffi import CSignature, TypedefMap
from alchemist.verifier.differential_tester import DifferentialConfig
from alchemist.verifier.proptest_gen import AlgorithmHarness


# ---------------------------------------------------------------------------
# C zlib FFI signatures
# ---------------------------------------------------------------------------

def zlib_public_signatures() -> list[CSignature]:
    """The subset of zlib's public API we exercise in Phase D."""
    return [
        CSignature(
            name="adler32",
            return_type="uLong",
            params=[
                ("adler", "uLong"),
                ("buf", "const Bytef *"),
                ("len", "uInt"),
            ],
        ),
        CSignature(
            name="crc32",
            return_type="uLong",
            params=[
                ("crc", "uLong"),
                ("buf", "const Bytef *"),
                ("len", "uInt"),
            ],
        ),
        CSignature(
            name="compress",
            return_type="int",
            params=[
                ("dest", "Bytef *"),
                ("destLen", "uLongf *"),
                ("source", "const Bytef *"),
                ("sourceLen", "uLong"),
            ],
        ),
        CSignature(
            name="uncompress",
            return_type="int",
            params=[
                ("dest", "Bytef *"),
                ("destLen", "uLongf *"),
                ("source", "const Bytef *"),
                ("sourceLen", "uLong"),
            ],
        ),
    ]


def zlib_typedefs() -> TypedefMap:
    """Zlib uses its own typedefs (uLong, Bytef, uInt). Map them to Rust."""
    # The auto_ffi C_SCALAR_MAP already knows uLong / Bytef / uInt. Returning an
    # empty TypedefMap keeps the default behavior.
    return TypedefMap()


# ---------------------------------------------------------------------------
# AlgorithmHarnesses — differential tests generated by proptest_gen
# ---------------------------------------------------------------------------

def zlib_harnesses() -> list[AlgorithmHarness]:
    """Per-algorithm differential harnesses for the zlib translation.

    Each harness assumes:
      - The Rust translation exposes top-level fns `rust_adler32`, `rust_crc32`,
        `rust_compress`, `rust_uncompress` in a `zlib_rs` glue crate OR
        reachable under the generated crates (zlib-checksum, zlib-compression).
      - The FFI crate `c_reference` exposes `c_adler32`, `c_crc32`,
        `c_compress`, `c_uncompress` with byte-slice / Vec<u8> shapes.

    The diff_test crate that differential_tester.py synthesizes will import
    from both — so these call expressions reference symbols at the crate root
    of `c_reference` and expect companion `rust_*` helpers to be defined in
    the diff_test crate's own `lib.rs` (added below).
    """
    return [
        AlgorithmHarness(
            algorithm="adler32",
            category="checksum",
            rust_call="rust_adler32(&input)",
            c_call="c_adler32(&input)",
            cases=5000,
        ),
        AlgorithmHarness(
            algorithm="crc32",
            category="checksum",
            rust_call="rust_crc32(&input)",
            c_call="c_crc32(&input)",
            cases=5000,
        ),
        AlgorithmHarness(
            algorithm="deflate",
            category="compression",
            rust_call="rust_compress(&input)",
            c_call="c_compress(&input)",
            rust_decompress_call="rust_uncompress(&compressed, input.len() + 64)",
            c_decompress_call="c_uncompress(&compressed, input.len() + 64)",
            cases=1000,
        ),
    ]


